thislist = ["orange", "mango", "kiwi", "pineapple", "banana"]

thislist.sort()

print(thislist) ## ['banana', 'kiwi', 'mango', 'orange', 'pineapple']

thislist.sort(reverse=True)
print(thislist) ## ['pineapple', 'orange', 'mango', 'kiwi', 'banana']


